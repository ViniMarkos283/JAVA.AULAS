
public class Calculadora {

	public static void main(String[] args) {
		
		// variáveis
		
		double n1 = 5;
		double n2 = 2;
		double resulS; // soma
		double resulSu; // subtração
		double resulM; // multiplicação
		double resulD; // Divisão
		
		// começo
		
		resulS = n1 + n2;  // formula da conta
		System.out.println( "O resultado da soma é " + resulS); //sintaxe que mostra a resposta
		
		resulSu = n1 - n2;
		System.out.println( "O resultado da subtração é " + resulSu);
	
		resulM = n1 * n2;
		System.out.println( "O resultado da multiplicação é " + resulM);
		
		resulD = n1 / n2;
		System.out.println( "O resultado da divisão é " + resulD);
		
		
	}

}
