
public class Matematica { // Aqui é onde vai ter as operaçoes

	public static int somar(int a, int b) { // o static convoca o atributo diretamente pela classe, sem convocar o metodo, usa quando não for alterar o objeto.
		return a+b;
	}
	
	public static int subtrair(int a, int b) {
		return a-b;
	}
	
}
